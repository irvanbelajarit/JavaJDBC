    private boolean EditData(){
          if(jTextFieldCariNIM.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Text Cari NIM Harus Diisi!", "Alert", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        try {
            stmt = conn.createStatement();
            
            int update = stmt.executeUpdate("UPDATE mahasiswa SET nim ='"+nim+"',nama ='"+nama+"',alamat='"+alamat+"',kodeprodi ='"+idProdi+"',kodefakultas ='"+idFakultas +"' "
                    + " WHERE nim = '"+cariNIM+"'");
            if(update ==1){
                JOptionPane.showMessageDialog(rootPane,"data berhasil Diubah");
            }
            return true;
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(null,"error" + ex.getMessage());
        }
        return false;
    }
    
