    private boolean hapusMahasiswa(){
         if(jTextFieldCariNIM.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Text Cari NIM Harus Diisi!", "Alert", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        try {
              stmt = conn.createStatement();
              int delete = stmt.executeUpdate("DELETE FROM mahasiswa WHERE nim ='"+nim+"'");
              if (delete ==1){
                  JOptionPane.showMessageDialog(rootPane,"data berhasil dihapus");
              }
              return true;
        } catch (SQLException e) {
             JOptionPane.showMessageDialog(rootPane, e);
        }
        return false;
    }
